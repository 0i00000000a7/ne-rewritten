import {describe, expect, it} from 'vitest'
import {BM4, display, is_infinite, type Expr} from '../notations/BM'

// ===========================================================================
// display 测试
// ===========================================================================

describe('display', () => {
    it('极限', () => {
        expect(display([[Infinity]])).toBe('Limit')
    })

    it('空矩阵', () => {
        expect(display([])).toBe('')
    })

    it('标准矩阵', () => {
        expect(display([[0], [0]])).toBe('(0)(0)')
        expect(display([[0, 0], [1, 1]])).toBe('(0,0)(1,1)')
        expect(display([[0, 0, 0], [1, 1, 1], [2, 1, 0], [1, 1, 1]])).toBe('(0,0,0)(1,1,1)(2,1,0)(1,1,1)')
    })
})

// ===========================================================================
// FS 展开测试
// ===========================================================================

describe('FS', () => {
    it('0 展开', () => {
        expect(BM4.FS([], 2)).toEqual([])
        expect(BM4.FS([], 4)).toEqual([])
    })

    it('后继展开', () => {
        expect(BM4.FS([[0], [0]], 2)).toEqual([[0]])
        expect(BM4.FS([[0, 0], [1, 1], [0, 0]], 4)).toEqual([[0, 0], [1, 1]])
    })

    it('2 行展开', () => {
        expect(BM4.FS([[0, 0], [1, 1], [1, 1]], 2))
            .toEqual([[0, 0], [1, 1], [1, 0], [2, 1], [2, 0], [3, 1]])
    })

    it('3 行提升展开', () => {
        expect(BM4.FS([[0, 0, 0], [1, 1, 1], [2, 1, 0], [1, 1, 1]], 1))
            .toEqual([[0, 0, 0], [1, 1, 1], [2, 1, 0], [1, 1, 0], [2, 2, 1], [3, 2, 0]])
        expect(BM4.FS([[0, 0, 0], [1, 1, 1], [2, 2, 0], [1, 1, 1]], 1))
            .toEqual([[0, 0, 0], [1, 1, 1], [2, 2, 0], [1, 1, 0], [2, 2, 1], [3, 3, 0]])
        expect(BM4.FS([[0, 0, 0], [1, 1, 1], [2, 2, 2], [3, 2, 0], [2, 2, 2]], 1))
            .toEqual([[0, 0, 0], [1, 1, 1], [2, 2, 2], [3, 2, 0], [2, 2, 1], [3, 3, 2], [4, 3, 0]])
        expect(BM4.FS([[0, 0, 0], [1, 1, 1], [2, 2, 2], [3, 1, 0], [2, 2, 2]], 1))
            .toEqual([[0, 0, 0], [1, 1, 1], [2, 2, 2], [3, 1, 0], [2, 2, 1], [3, 3, 2], [4, 1, 0]])
    })
})